#include <stdio.h>

int main() {
    //Signed integers
    char c = 0;
    short s = 1;
    int i = 2;
    long l = 3;
    long long ll = 4;

    //Unsigned integers
    unsigned char uc = 0;
    unsigned short us = 1;
    unsigned int ui = 2;
    unsigned long ul = 3;
    unsigned long long ull = 4;

    //Print the unsigned character.
    printf("uc = %d\n", uc);

    //Floating Point Types
    float f = 3.14;
    double d = 2.7181;
    long double ld = 1.0;

    return 0;
}